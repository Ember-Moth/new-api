async function main() {
  const statusUrl = resolveStatusUrl()
  const brand = await fetchBrand(statusUrl)

  await fs.mkdir(OUTPUT_DIR, { recursive: true })
  await fs.writeFile(JSON_OUTPUT, `${JSON.stringify(brand, null, 2)}\n`)
  await fs.writeFile(TS_OUTPUT, renderTypeScript(brand))

  console.info(
    `[brand-bootstrap] generated ${path.relative(process.cwd(), TS_OUTPUT)} (${brand.source})`
  )
}

main().catch((error) => {
  console.error(error)
  process.exitCode = 1
})
