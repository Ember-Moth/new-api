import fs from 'node:fs/promises'
import path from 'node:path'

const DEFAULT_BRAND = {
  systemName: 'New API',
  logo: '/logo.png',
  description: 'Unified AI API gateway and admin dashboard.',
  source: 'default',
  generatedAt: null,
}

const OUTPUT_DIR = path.resolve('src/generated')
const JSON_OUTPUT = path.join(OUTPUT_DIR, 'brand-bootstrap.json')
const TS_OUTPUT = path.join(OUTPUT_DIR, 'brand-bootstrap.ts')
const STATUS_PATH = '/api/status'
const DEFAULT_TIMEOUT_MS = 5000
const SKIP_FETCH = process.argv.includes('--skip-fetch')

function trimString(value) {
  return typeof value === 'string' ? value.trim() : ''
}

function parseTimeoutMs() {
  const raw = Number(process.env.BRAND_BOOTSTRAP_TIMEOUT_MS)
  if (Number.isFinite(raw) && raw > 0) return raw
  return DEFAULT_TIMEOUT_MS
}

function ensureStatusUrl(value, explicit) {
  const raw = trimString(value)
  if (!raw) return ''

  try {
    const url = new URL(raw)
    if (explicit) {
      if (url.pathname === '' || url.pathname === '/') {
        url.pathname = STATUS_PATH
      }
      return url.toString()
    }

    url.pathname = STATUS_PATH
    url.search = ''
    url.hash = ''
    return url.toString()
  } catch {
    return ''
  }
}

function resolveStatusUrl() {
  const explicit = ensureStatusUrl(
    process.env.BRAND_BOOTSTRAP_STATUS_URL,
    true
  )
  if (explicit) return explicit

  return ensureStatusUrl(
    process.env.BRAND_BOOTSTRAP_ORIGIN ||
      process.env.VITE_REACT_APP_SERVER_URL ||
      process.env.BACKEND_ORIGIN,
    false
  )
}

function normalizeStatusPayload(payload, source) {
  const data =
    payload && typeof payload === 'object' && payload.data
      ? payload.data
      : payload

  if (!data || typeof data !== 'object') {
    return { ...DEFAULT_BRAND, source }
  }

  return {
    systemName: trimString(data.system_name) || DEFAULT_BRAND.systemName,
    logo: trimString(data.logo) || DEFAULT_BRAND.logo,
    description:
      trimString(data.description) ||
      trimString(data.site_description) ||
      DEFAULT_BRAND.description,
    source,
    generatedAt: new Date().toISOString(),
  }
}

async function fetchBrand(statusUrl) {
  if (SKIP_FETCH || !statusUrl) return { ...DEFAULT_BRAND }

  const controller = new AbortController()
  const timeout = setTimeout(() => controller.abort(), parseTimeoutMs())

  try {
    const response = await fetch(statusUrl, {
      headers: { Accept: 'application/json' },
      signal: controller.signal,
    })
    if (!response.ok) {
      throw new Error(`HTTP ${response.status}`)
    }
    const payload = await response.json()
    return normalizeStatusPayload(payload, statusUrl)
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error)
    console.warn(
      `[brand-bootstrap] failed to fetch ${statusUrl}, using defaults: ${message}`
    )
    return { ...DEFAULT_BRAND }
  } finally {
    clearTimeout(timeout)
  }
}

